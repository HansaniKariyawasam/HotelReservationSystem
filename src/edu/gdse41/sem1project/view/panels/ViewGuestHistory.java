    private void CBAgentItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_CBAgentItemStateChanged
        String agent=(String) CBAgent.getSelectedItem();
        if(agent!="Default"){
            CBID.removeAllItems();
            try {
                ArrayList<String> ids=ctrlGuest.getAllGuestHistroyIDs(agent);
                for (String id : ids) {
                    CBID.addItem(id);
                }
            } catch (SQLException ex) {
                Logger.getLogger(AdminViewGuestHistory.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AdminViewGuestHistory.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            CBID.removeAllItems();
            try {
                ArrayList<String> ids=ctrlGuest.getAllGuestHistroyIDs(null);
                for (String id : ids) {
                    CBID.addItem(id);
                }
            } catch (SQLException ex) {
                Logger.getLogger(AdminViewGuestHistory.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AdminViewGuestHistory.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_CBAgentItemStateChanged

