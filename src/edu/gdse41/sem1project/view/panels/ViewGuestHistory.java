    private void lblSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblSaveMouseClicked
        GuestDTO guest=new GuestDTO((String)CBID.getSelectedItem(),
            txtf_name.getText(),
            txtl_name.getText(),
            txtdob.getText(),
            txtnatinality.getText(),
            txtNIC.getText(),
            txtpassport.getText(),
            txttel.getText());
        try {
            boolean result=ctrlGuest.update(guest);
            if(result){
                DefaultTableModel dtm=(DefaultTableModel) detailTable.getModel();
                for(int i=0;i<detailTable.getRowCount();i++){
                    String id=(String) CBID.getSelectedItem();
                    if(id == null ? (String)dtm.getValueAt(i, 0) == null : id.equals((String)dtm.getValueAt(i, 0))){
                        dtm.setValueAt(txtf_name.getText(), i, 2);
                        dtm.setValueAt(txtl_name.getText(), i, 3);
                        dtm.setValueAt(txtdob.getText(), i, 4);
                        dtm.setValueAt(txtnatinality.getText(), i, 5);
                        dtm.setValueAt(txtNIC.getText(), i, 6);
                        dtm.setValueAt(txtpassport.getText(), i, 7);
                        dtm.setValueAt(txttel.getText(), i, 8);
                    }
                }
                JOptionPane.showMessageDialog(this, "Saved changes");
            }
        } catch (Exception ex) {
            Logger.getLogger(AdminViewGuestHistory.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_lblSaveMouseClicked

