    private void lblConfirmMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblConfirmMouseClicked
        DefaultTableModel dtm=(DefaultTableModel) roomTable.getModel();
        double amount=0;
        for(int i=0;i<roomTable.getRowCount();i++){
            amount=amount+(double)dtm.getValueAt(i, 3);
        }
        
        String pid;
        try {
            pid = IDGenerator.getNewID("Payment", "PID", "P");
            String resId=txtResID.getText();
            String curDate=txtResDate.getText();
            String agent=AgentAddGuest.agent;
            double amountPaid;
            if(agent!="Booking.Com"){
                amountPaid=amount*Double.parseDouble(txtNoOfNights1.getText());
            }else{
                amountPaid=0;
            }
            PaymentDTO p1=new PaymentDTO(pid,resId,amountPaid,curDate,null);
            boolean result=ctrlPayment.add(p1);
        } catch (SQLException ex) {
            Logger.getLogger(AgentReservation.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AgentReservation.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(AgentReservation.class.getName()).log(Level.SEVERE, null, ex);
        }
        Availability.rows.clear();
    }//GEN-LAST:event_lblConfirmMouseClicked

