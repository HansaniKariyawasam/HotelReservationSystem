    private void loadTableData(){
        ArrayList<Object[]> rows=AgentReserveRoom.rows;
        for(Object[] row:rows){
            DefaultTableModel dtm=(DefaultTableModel)roomTable.getModel();
            dtm.addRow(row);
        }
    }
    
    private void calculateNights(){
        String checkIn=AgentAddGuest.checkIn;
        String checkOut=AgentAddGuest.checkOut;
        LocalDate cIn=LocalDate.parse(checkIn);
        LocalDate cOut=LocalDate.parse(checkOut);
        int nights=cOut.compareTo(cIn);
        txtNoOfNights1.setText(""+nights);
    }
    
    private void setReservationDate(){
        Date d=new Date();
        SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");
        String date=sdf.format(d);
        txtResDate.setText(date);
    }
    
